const AnonTypes = struct {
    types: StringPool.HashMap(json.ObjectMap),
    pub fn init() AnonTypes {
        return .{
            .types = StringPool.HashMap(json.ObjectMap).init(allocator),
        };
    }
    pub fn deinit(self: *AnonTypes) void {
        self.types.deinit();
